        public int WeeksBack { get; set; }

        [Parameter(DefaultValue = "http://darkmindfx.com/")]
        public string Host { get; set; }

        [Output("Prod_Merc_Positions_Net", LineColor = "Red")]
        public IndicatorDataSeries Prod_Merc_Positions_Net { get; set; }

